# ===== Переменные для генерации =====
titles = [
    "сделать отчет", "купить молоко", "подготовить презентацию",
    "позвонить клиенту", "закончить лабораторную"
]

projects = [
    "ДЗ", "Проект A", "Учебный проект", "Рабочий проект"
]

priorities = ["низкий", "средний", "высокий"]

descriptions = [
    "лаборато́рная работа по ТАТД",
    "сделать отчет по лабораторной",
    "слайды для семинара",
    "позвонить клиенту и уточнить детали"
]

# ===== Функция генерации случайной даты =====
def random_due_date():
    days = random.randint(1, 30)
    hours = random.randint(0, 23)
    return (datetime.utcnow() + timedelta(days=days, hours=hours)).strftime("%Y-%m-%dT%H:%M:%SZ")

def generate_dataset(n_per_class=500):
    data = []
    for label, tmpl_list in templates.items():
        for _ in range(n_per_class):
            tmpl = random.choice(tmpl_list)
            
            if label in ["TASK_CREATE", "TASK_UPDATE", "TASK_DELETE"]:
                title = random.choice(titles)
                text = tmpl.format(title)
                
                if label == "TASK_CREATE":
                    # 50% примеров короткие
                    if random.random() < 0.5:
                        # короткий пример — просто команда
                        text = tmpl.format(title)
                    else:
                        # длинный пример с деталями
                        project = random.choice(projects)
                        priority = random.choice(priorities)
                        description = random.choice(descriptions)
                        due_date = random_due_date()
                        text += f" до {due_date}, приоритет {priority}, проект {project}, описание: {description}"
                        
                        # 30% примеров — дочерняя задача
                        if random.random() < 0.3:
                            parent_title = random.choice(titles)
                            text += f", как дочернюю к задаче {parent_title}"
            else:
                text = tmpl

            data.append((text, label))
    return pd.DataFrame(data, columns=["text", "label"])

# ===== Генерация и сохранение =====
df = generate_dataset(n_per_class=2000)
df = df.sample(frac=1).reset_index(drop=True)
df.to_csv("data.csv", index=False)

